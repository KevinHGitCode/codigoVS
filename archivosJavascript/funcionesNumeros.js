// Este archivo contiene una serie de funciones de manipulación de numeros

var numero = 10;

numero.toString(); // Devuelve un string con el número indicado

numero.toFixed(2); // Devuelve un string con el número indicado, con dos decimales

numero.toExponential(2); // --

numero.toPrecision(2); // --

numero.valueOf(); // Devuelve el número indicado

numero.isFinite(); // Devuelve true si el número es finito

numero.isNaN(); // Devuelve true si el número es NaN

numero.isInteger(); // Devuelve true si el número es entero

numero.isSafeInteger(); // Devuelve true si el número es entero y está dentro del rango de valores de un entero de 32 bits

